// Parses scratchpad service HTML into structured data via node-html-parser.
// Writes src/content/services-data.ts. Dev-only tool.
import { readFile, writeFile } from "node:fs/promises";
import { parse } from "node-html-parser";

const SRC =
  "/tmp/user/1000/claude-1000/-home-hunt-Documents-Ubuntu-AGY-ai-website-cloner-template/9a156f73-fb4b-4bea-8048-74a3bffd4cd3/scratchpad/pages";

const SERVICE_FILES = [
  ["vykup-avto-v-lyubom-sostoyanii", "s-sostoyanie"],
  ["vykup-avto-na-zapchasti", "s-zapchasti"],
  ["vykup-gruzovyh-avto", "s-gruz"],
  ["vykup-zalogovyh-avto", "s-zalog"],
  ["vykup-kreditnyh-avto", "s-kredit"],
  ["vykup-nerastamozhennyh-avto-na-evronomerah", "s-nerast"],
  ["vykup-problemnyh-avto", "s-problem"],
  ["vykup-staryh-avto", "s-staryh"],
  ["oczenka-avto-onlajn", "s-ocenka"],
  ["vykup-avto-bez-dokumentov-v-kieve", "s-bezdok"],
];

const STOP =
  /Отзывы|Вопрос-ответ|Популярные вопросы|Какие марки|Оставить заявку|Мы занимаемся авто/i;
const NOISE =
  /Узнайте стоимость своего авто|Получите максимальную сумму|^Марка автомобиля$|^Модель автомобиля$|^Год выпуска$|^Ваш телефон$|^Дополнительная информация$|^Узнать стоимость$/i;
const norm = (s) => s.replace(/\s+/g, " ").trim();

function collect(html) {
  const root = parse(html);
  const content = root.querySelector("#Content") || root;
  const nodes = content.querySelectorAll("h1,h2,h3,h4,p,ul,ol,table");
  let title = "";
  const intro = [];
  const sections = [];
  let table = null;
  let cur = null;
  for (const el of nodes) {
    const tag = el.tagName.toLowerCase();
    if (el.closest("form")) continue;
    const txt = norm(el.text);
    if (/h[1-4]/.test(tag)) {
      if (!txt || NOISE.test(txt)) continue;
      if (STOP.test(txt)) break;
      if (tag === "h1") {
        title = txt;
        continue;
      }
      cur = { heading: txt, items: [] };
      sections.push(cur);
      continue;
    }
    if (tag === "table") {
      const rows = el
        .querySelectorAll("tr")
        .map((tr) =>
          tr.querySelectorAll("td,th").map((c) => norm(c.text)),
        )
        .filter((r) => r.some((x) => x));
      if (rows.length >= 2 && !table)
        table = { headers: rows[0], rows: rows.slice(1) };
      continue;
    }
    if (tag === "ul" || tag === "ol") {
      if (el.closest("table")) continue;
      const lis = el
        .childNodes.filter((c) => c.tagName === "LI")
        .map((li) => norm(li.text))
        .filter((x) => x.length > 1 && !NOISE.test(x));
      if (!lis.length) continue;
      (cur ? cur.items : intro).push({ t: "list", items: lis });
      continue;
    }
    if (tag === "p") {
      if (el.closest("table") || el.closest("ul") || el.closest("ol")) continue;
      if (txt.length < 2 || NOISE.test(txt)) continue;
      (cur ? cur.items : intro).push({ t: "p", text: txt });
    }
  }
  return { title, intro, sections, table };
}

const out = {};
for (const [slug, file] of SERVICE_FILES) {
  const html = await readFile(`${SRC}/${file}.html`, "utf8");
  const data = collect(html);
  out[slug] = data;
  console.log(
    `${slug}: intro=${data.intro.length} sections=${data.sections.length} table=${data.table ? data.table.rows.length + "r" : "-"} | ${data.title}`,
  );
}

const banner = `// AUTO-GENERATED by scripts/extract-services.mjs — content extracted from avtocare.com.ua\nimport type { ServicePageData } from "@/types";\n\n`;
const body = `export const SERVICE_PAGES: Record<string, ServicePageData> = ${JSON.stringify(out, null, 2)};\n`;
await writeFile("src/content/services-data.ts", banner + body, "utf8");
console.log("written src/content/services-data.ts");
